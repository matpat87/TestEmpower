            ['vi_vendorissues.vp_vendorproducts_id_c', 'vi_vendorissues.user_id_c'], 
            ['vi_vendorissues_cstm.vp_vendorproducts_id_c', 'vi_vendorissues_cstm.user_id_c'], 
            $result
        );
        
        return $result;
    }
	
}